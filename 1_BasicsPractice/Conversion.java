import java.util.*;

public class Conversion {
    public static void main(String args[]) {
        Scanner sc = new Scanner(System.in);

        // _________________
        float a = 25.12f;
        int b = (int) a;

        System.out.println(b);

        // _________________
        char x = 'a';
        int y = x;

        System.out.println(y);

        // _________________
        int $ = 24;
        System.out.println($);

        sc.close();
    }
}
